package basic;

public class JavaLoop {
	 public static void main(String [] args) {
		 
		 // for loop
		 
		 for(int i=1; i<=5;i++)
		 {
			 System.out.println("Sarvesh");		
			 
		 }
		 
		 // while loop
		 
		 int a=10;
		 while (a>=1) {
			 System.out.println("=");
			 a--;
		 }
		 
		 // do while loop
		 
		 int b=0;
		 do {
			 System.out.println(b);
			 b--;
		 }
			 while(b>=1);
		 }
	 }

